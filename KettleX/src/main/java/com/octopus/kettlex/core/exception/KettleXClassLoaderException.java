package com.octopus.kettlex.core.exception;

public class KettleXClassLoaderException extends KettleXException {

  public KettleXClassLoaderException() {
    super();
  }

  public KettleXClassLoaderException(String message) {
    super(message);
  }

  public KettleXClassLoaderException(Throwable cause) {
    super(cause);
  }

  public KettleXClassLoaderException(String message, Throwable cause) {
    super(message, cause);
  }
}
